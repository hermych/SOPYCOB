function validarDatos(datos) {
  respuesta = {};
  datos.forEach((dato) => {
    if (dato.name == 'sucursal' || dato.name == 'comprobante' || dato.name == 'sucursalEdit' || dato.name == 'comprobanteEdit') {
      if (dato.value == 0 || dato.value == '0') {
        respuesta[dato.name] = `El campo ${dato.name} es obligatorio`;
      }
    } else if (dato.value == "" || dato.value.length < 1) {
      respuesta[dato.name] = `El campo ${dato.name} es obligatorio`;
    }
  });
  return respuesta;
}
function validarInputSoloNumeros(evt) {
  let code = (evt.which) ? evt.which : evt.keyCode;
  if (code == 8) { // backspace.
    return true;
  } else if (code >= 48 && code <= 57) { // is a number.
    return true;
  } else { // other keys.
    return false;
  }
}
