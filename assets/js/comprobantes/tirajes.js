$("#btnAbrirmodalGuardarTiraje").click(() => {
  let selectSucursal = document.getElementById('sucursal');
  window.$.ajax({
    type: "get",
    url: "TirajesController.php?method=listarSucursales",
    beforeSend: function () {
      Swal.fire({
        allowOutsideClick: false,
        title: 'Cargando',
        timerProgressBar: true,
        didOpen: () => {
          Swal.showLoading()
          const b = Swal.getHtmlContainer().querySelector('b')
          timerInterval = setInterval(() => {
          }, 300)
        }
      })
    },
    success: function (response) {
      Swal.close()
      let data = JSON.parse(response);
      contenido = "<option value='0'>Sucursales</option>";
      data.forEach((prov) => {
        contenido += `<option value=${prov.idSucursal}>${prov.nombre}</option>`;
      });
      selectSucursal.innerHTML = contenido;
    },
  });
  let sucursal = document.getElementById("sucursal");
  let comprobante = document.getElementById("comprobante");
  let serie = document.getElementById("serie");
  let desde = document.getElementById("desde");
  let hasta = document.getElementById("hasta");
  datosArray = [
    sucursal, comprobante, serie, desde, hasta
  ];
  setInterval(() => {
    let respuesta = validarDatos(datosArray);
    if (Object.keys(respuesta).length == 0) {
      $("#btnGuardarTiraje").prop("disabled", false);
    } else {
      $("#btnGuardarTiraje").prop("disabled", true);
    }
  }, 200);
});
