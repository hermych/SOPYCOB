function validarInputSoloNumeros(evt) {
  let code = evt.which ? evt.which : evt.keyCode;
  if (code == 8) {
    // backspace.
    return true;
  } else if (code >= 48 && code <= 57 || code <= 46) {
    // is a number.
    return true;
  } else {
    // other keys.
    return false;
  }
}
function reiniciarModalGuardar() {
  $('#nombre').removeClass('border-success');
  $('#nombre').addClass('border-danger');
  $('#nombre').val('');
  $('#precio').removeClass('border-success');
  $('#precio').addClass('border-danger');
  $('#precio').val('');
  $('#btnGuardar').prop('disabled', true);
}
function reiniciarModalEditar() {
  $('#nombreEdit').removeClass('border-danger');
  $('#nombreEdit').addClass('border-success');
  $('#nombreEdit').val('');
  $('#precioEdit').removeClass('border-danger');
  $('#precioEdit').addClass('border-success');
  $('#precioEdit').val('');
  $('#btnEditar').prop('disabled', false);
}
// ------ cerrar modales -------
$('#btnCerrarGuardar').click(function () {
  reiniciarModalGuardar()
})
$('#btnCerrarEditar').click(function () {
  reiniciarModalEditar()
})
// ------ validar datos formulario agregar -------
$('#nombre').keyup(function () {
  if ($('#nombre').val().length >= 3) {
    $('#nombre').removeClass('border-danger')
    $('#nombre').addClass('border-success')
  } else {
    $('#nombre').removeClass('border-success')
    $('#nombre').addClass('border-danger')
  }
});
$('#precio').keyup(function () {
  if ($('#precio').val().length >= 3) {
    $('#precio').removeClass('border-danger')
    $('#precio').addClass('border-success')
  } else {
    $('#precio').removeClass('border-success')
    $('#precio').addClass('border-danger')
  }
});
setInterval(() => {
  if ($('#precio').hasClass('border-success') && $('#nombre').hasClass('border-success')) {
    $('#btnGuardar').prop('disabled', false)
  } else {
    $('#btnGuardar').prop('disabled', true)
  }
}, 150);
