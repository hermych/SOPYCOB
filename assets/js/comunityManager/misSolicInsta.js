function reenviarSolicInst(formData, dataTable) {
  window.$.ajax({
    type: "post",
    url: "InstalacionController.php?method=reenviarSolicInsta",
    data: formData,
    cache: false,
    processData: false,
    contentType: false,
    beforeSend: function () {
      Swal.fire({
        allowOutsideClick: false,
        title: 'Cargando',
        timerProgressBar: true,
        didOpen: () => {
          Swal.showLoading()
          const b = Swal.getHtmlContainer().querySelector('b')
          timerInterval = setInterval(() => {
          }, 300)
        }
      })
    },
    success: function (response) {
      Swal.close()
      let data = JSON.parse(response);
      if (data.estado == "ok") {
        dataTable.ajax.reload();
        $('#modalInstalacion').modal('hide');
        //$('.modal-backdrop').remove();
        $('#logo_1').val('');
        $('#logo_2').val('');
        Swal.fire({
          text: data.mensaje,
          icon: 'success',
        })
        setTimeout(() => {
          Swal.close()
        }, 1500);
      } else {
        Swal.fire({
          text: data.mensaje,
          icon: 'error',
        })
        setTimeout(() => {
          Swal.close()
        }, 1500);
      }
    },
  });
}
